print(1234)

#Aspas simples
print('1234', 'Texto')
print('Roni Santos')
print('Roni "Santos"')

#Aspas duplas
print("'1234', 'Texto'")
print("Roni Santos")

#Escape
print('Roni \'Santos\'')
print("Roni \"Santos\"")
print("Roni \nSantos")
print("Roni \tSantos")
print("Roni \\ Santos")

#r
print(r"Roni \n Santos")

